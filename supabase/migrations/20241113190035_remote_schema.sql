CREATE OR REPLACE FUNCTION "public"."get_lessons_with_purchase"("user_id" "uuid") RETURNS TABLE("id" "uuid", "title" "text", "description" "text", "price" numeric, "content_url" "text", "creator_id" "uuid", "created_at" timestamp with time zone, "updated_at" timestamp with time zone, "is_purchased" boolean)
    LANGUAGE "plpgsql" STABLE
    AS $$
begin
  return query
  select t.*, 
    exists (
      select 1 from purchases p 
      where p.tutorial_id = t.id and p.user_id = user_id
    ) as is_purchased
  from tutorials t;
end;
$$;


