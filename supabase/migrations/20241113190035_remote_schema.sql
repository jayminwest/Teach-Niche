CREATE OR REPLACE FUNCTION "public"."update_lesson_and_categories"("p_lesson_id" "uuid", "p_title" "text", "p_description" "text", "p_price" numeric, "p_video_url" "text", "p_content" "jsonb", "p_thumbnail_url" "text", "p_category_ids" integer[]) RETURNS "void"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
  v_old_category_ids INTEGER[];
BEGIN
  -- Update the lesson
  UPDATE tutorials
  SET 
    title = p_title,
    description = p_description,
    price = p_price,
    video_url = p_video_url,
    content = p_content,
    thumbnail_url = p_thumbnail_url,
    updated_at = NOW()
  WHERE id = p_lesson_id;

  -- Get existing category IDs
  SELECT ARRAY_AGG(category_id) INTO v_old_category_ids
  FROM tutorial_categories
  WHERE tutorial_id = p_lesson_id;

  -- Remove categories that are no longer associated
  DELETE FROM tutorial_categories
  WHERE tutorial_id = p_lesson_id
    AND category_id = ANY(v_old_category_ids)
    AND category_id != ALL(p_category_ids);

  -- Insert new category associations
  INSERT INTO tutorial_categories (tutorial_id, category_id)
  SELECT p_lesson_id, unnest(p_category_ids)
  ON CONFLICT (tutorial_id, category_id) DO NOTHING;
END;
$$;


